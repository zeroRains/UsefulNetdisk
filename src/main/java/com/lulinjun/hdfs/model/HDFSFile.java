package com.lulinjun.hdfs.model;

public class HDFSFile {
    private String name;
    private String type;
    private long len;

    public HDFSFile() {
    }

    public HDFSFile(String name, String type, long len) {
        this.name = name;
        this.type = type;
        this.len = len;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public long getLen() {
        return len;
    }

    public void setLen(long len) {
        this.len = len;
    }
}
